#include "libft.h"
#include <stdlib.h>

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*alst_next;

	while (*alst != NULL)
	{
		alst_next = (*alst)->next;
		(*del)((*alst)->content, (*alst)->content_size);
		free(*alst);
		*alst = alst_next;
	}
	*alst = NULL;
}
