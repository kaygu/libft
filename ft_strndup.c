#include <stdlib.h>
#include "libft.h"

char	*ft_strndup(const char *s, size_t len)
{
	char	*dst;

	if ((dst = ft_strnew(len)))
	{
		ft_memcpy(dst, s, len);
	}
	return (dst);
}
